import java.util.Scanner;
public class demo01 {
    /*变量：内存中的存储空间，空间中记录这经常发生改变的数据
    变量的定义格式：
              数据类型 变量名 = 数据值；
              数据类型：整数：int
       变量的注意事项：
            1.变量名不允许重复定义
            2.一条语句，可以定义出多个变量，中间需要使用逗号分隔
            3.变量在使用前，必须完成赋值
           TODO 4.变量的作用域
     */
    public static void main(String[] args) {
      int salary=120000;
      //=为赋值号，将右侧的赋值给左侧；
        System.out.println(salary);
    }
}
